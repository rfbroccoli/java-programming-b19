package assignment;

/**
 * Main
 */
public class Main {

    public static void main(String[] args) {
        int num1 = 234;
        int num2 = 212;
        System.out.printf("num1 = %d%n", num1);
        System.out.printf("num2 = %d%n", num2);
        // addition အပေါင်း
        System.out.printf("num1 + num2 = %d%n", num1 + num2);
        // substraction အနုတ်
        System.out.printf("num1 - num2 = %d%n", num1 - num2);
        // multiplication အမြှောက်
        System.out.printf("num1 * num2 = %d%n", num1 * num2);
        // division အစား
        System.out.printf("num1 / num2 = %d%n", num1 / num2);
        // division and conversion to float အစား
        System.out.printf("(float) num1 / num2 = %f%n", (float) num1 / num2);
        // modulus အကြွင်း
        System.out.printf("num1 %% num2 = %d%n", num1 % num2);


    
    }
}