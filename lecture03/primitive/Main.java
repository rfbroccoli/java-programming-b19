public class Main {
    public static void main(String[] args) {
        // boolean
        boolean isGameOver = false;
        System.out.printf("isGameOver: %b%n", isGameOver);

        // char
        char userInput = 'n';
        System.out.printf("userInput: %c%n", userInput);
        // System.out.printf("userInput: %c%n%b", userInput, true);

        // byte
        byte numOfStudents = 127;
        System.out.printf("numOfStudents: %d%n", numOfStudents);

        // short
        short numOfInsects = 32767;
        System.out.printf("numOfInsects: %d%n", numOfInsects);

        // int
        int numOfMolecules = 2147483647;
        System.out.printf("numOfMolecules: %d%n", numOfMolecules);

        // long
        long numOfAtoms = 2147483647;
        System.out.printf("numOfAtoms: %d%n", numOfAtoms);
        
        // float
        float speedOfTheCar = 214.7483f;
        System.out.printf("speedOfTheCar: %f%n", speedOfTheCar);
        
        // double
        double speedOfTheElectron = 2147.483f;
        System.out.printf("speedOfTheElectron: %f%n", speedOfTheElectron);
    }
}
